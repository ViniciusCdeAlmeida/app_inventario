import 'package:app_inventario/helpers/helper_caracteristicaEst.dart';
import 'package:app_inventario/models/materialCaracteristica.dart';

MaterialCaracteristica helperMaterialCaracteristica(
    Map listaMatCaracteristica) {
  return MaterialCaracteristica(
    id: listaMatCaracteristica['id'],
    codigo: listaMatCaracteristica['codigo'],
    descricao: listaMatCaracteristica['descricao'],
    caracteristica: helperCaracteristica(
      listaMatCaracteristica['caracteristica'],
    ),
  );
}
